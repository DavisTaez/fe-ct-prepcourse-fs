function sumarLikesDeUsuario(objetoUsuario) {
   // El parámetro "objetoUsuario" tiene una propiedad llamada "posts" que es un arreglo.
   // Este arreglo contiene objetos (post).
   // Cada post posee una propiedad llamada "likes". Esta propiedad es un número.
   // Debes sumar los likes de todos los post y retornar el resultado.
   // Tu código:
   // !En esta función se utiliza foreach para recorrer un arreglo dentro del objeto
   // !Posterior a ello se realiza una operación hayando la propiedad y sumando sus valores.
   var sumatotal = 0
   objetoUsuario.posts.forEach((element) => {
      sumatotal += element.likes;
   });
   return sumatotal
}

